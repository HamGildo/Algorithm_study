package Algorithm_week11;

public class Edge {
    private int v1;
    private int v2;
    private int w;

    public Edge(int v1, int v2, int w) {
        this.v1 = v1;
        this.v2 = v2;
        this.w = w;
    }

    public int getV1() {
        return v1;
    }

    public int getV2() {
        return v2;
    }

    public int getW() {
        return w;
    }
}
